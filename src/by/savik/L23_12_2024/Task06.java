package by.savik.L23_12_2024;
/*
Используя цикл for вывести на экран:
        - горизонтальную линию из 10 восьмёрок
- вертикальную линию из 10 восьмёрок.
*/
public class Task06 {
    public static void main(String[] args) {
        int a = 8;
        for (int i = 0; i < 10; i++) {
            System.out.print(a);
        }
        for (int i = 0; i < 10; i++) {
            System.out.println(a);
        }
    }
}
